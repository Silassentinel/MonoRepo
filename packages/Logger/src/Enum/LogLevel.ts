/**
 * This enum contains the available loglevels
 */

enum LogLevel
{
    /**
     * This is the debug loglevel
     */
    Debug = 0,
    /**
     * This is the info loglevel
     */
    Info = 1,
    /**
     * This is the warning loglevel
     */
    Warning = 2,
    /**
     * This is the error loglevel
     */
    Error = 3,
    /**
     * This is the fatal loglevel
     */
    Fatal = 4,
}

export default LogLevel;
